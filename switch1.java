class switch1
{
	public static void main(String ar[])
	{
		int a=2;
		switch(a)
		{
			case 1:
				System.out.println("red");
				break;
			case 2:
				System.out.println("Silver");
				break;
			default:
				System.out.println("Blue");
				break;
		}
		System.out.println(a);
	}
}